var v1 = 3.5;

console.log("Arredonda para cima: " + Math.ceil(v1));
console.log("Arredonda para baixo: " + Math.floor(v1));
console.log("Arredonda casas decimais: " + Math.round(v1));
console.log("Valor absoluto: " + Math.abs(v1 * -1));  // retira o sinal (retorna sem sinal)
console.log("Raiz Quadrada " + Math.sqrt(64));  
console.log("Elevado ao 3: " + Math.pow(3, 3));  

